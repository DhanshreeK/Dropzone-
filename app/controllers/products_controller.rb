    @products = Product.all
  end

  # GET /products/1
  # GET /products/1.json
  def show
  end

  def export
    @product = Spreadsheet::Workbook.new
    sheet1 = @product.create_worksheet :name => 'Sheet1'
    sheet2 = @product.create_worksheet :name => 'Sheet2'
    sheet1.row(0).push("name","price","quantity")
    @products = Product.all
    @products.each do |p|
      sheet1.row(1).push(p.name)
      sheet1.row(1).push(p.price)
      sheet1.row(1).push(p.quantity)
      sheet2.row(0).push("name","price")
      spreadsheet = StringIO.new
      @product.write spreadsheet
      file = "Excelsheet"
      send_data spreadsheet.string, :filename => "#{file}", :type =>  "application/vnd.ms-excel"
  end
end

  # GET /products/new
  def new
    @product = Product.new
  end

  # GET /products/1/edit
  def edit
  end

  # POST /products
  # POST /products.json
  def create
    @product = Product.new(product_params)

    respond_to do |format|
      if @product.save
        format.html { redirect_to @product, notice: 'Product was successfully created.' }
        format.json { render :show, status: :created, location: @product }
      else
        format.html { render :new }
        format.json { render json: @product.errors, status: :unprocessable_entity }
      end
    end
  end

  # PATCH/PUT /products/1
  # PATCH/PUT /products/1.json
  def update
    respond_to do |format|
      if @product.update(product_params)
        format.html { redirect_to @product, notice: 'Product was successfully updated.' }
        format.json { render :show, status: :ok, location: @product }
      else
        format.html { render :edit }
        format.json { render json: @product.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /products/1
  # DELETE /products/1.json
  def destroy
    @product.destroy
    respond_to do |format|
      format.html { redirect_to products_url, notice: 'Product was successfully destroyed.' }
      format.json { head :no_content }
    end
  end

  private
    # Use callbacks to share common setup or constraints between actions.
    def set_product
      @product = Product.find(params[:id])
    end

    # Never trust parameters from the scary internet, only allow the white list through.
    def product_params
      params.require(:product).permit(:name, :price, :quantity)
    end
end
